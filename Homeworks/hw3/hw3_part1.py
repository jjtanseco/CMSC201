def main():

    #asks for number here
    number = input("Please enter a floating point number: " )
    number = float(number)

    #checks if number = 0
    if number == 0:
        print("The number ", number, " is equal to zero")
    
    #checks if number is less than 0
    if number < 0:
        print("The number ", number, " is negative")

    #checks if number is higher than zero
    if number > 0:
        print("The number ", number, " is positive")

main()
